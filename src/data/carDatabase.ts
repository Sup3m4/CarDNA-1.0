export function getEngineProfile(
  brand: string,
  model: string,
  generation: string,
  engineCode: string
): EngineProfile | undefined {
  return engineProfiles.find(
    (p) =>
      p.brand === brand &&
      p.model === model &&
      p.generation === generation &&
      p.engineCode === engineCode
  );
}
